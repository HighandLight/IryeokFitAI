    const resumeFile = resumeInput.files[0];
    const userId = localStorage.getItem("userId");

    const formData = new FormData();
    formData.append("userId", userId);
    formData.append("file", resumeFile);

    let uploadedResume;
    try {
        const uploadResponse = await fetch("/resumes/upload", {
            method: "POST",
            headers: {
                Authorization: `Bearer ${localStorage.getItem("token")}`
            },
            body: formData,
        });
        uploadedResume = await uploadResponse.json();
    } catch (error) {
        console.error("Ïù¥Î†•ÏÑú ÏóÖÎ°úÎìú Ïã§Ìå®:", error);
        proceedButton.innerHTML = "‚úÖ ÎÑ§, ÏßÑÌñâÌï†Í≤åÏöî";
        proceedButton.disabled = false;
        return;
    }

    const jobPostingData = window.jobPostingData || {};

