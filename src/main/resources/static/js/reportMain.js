document.getElementById("analyzeButton").addEventListener("click", async () => {
    const jobUrl = document.getElementById("jobUrl").value.trim();
    const resumeInput = document.getElementById("resumeUpload");
    if (!jobUrl) return showUrlErrorModal();
    if (!resumeInput.files || resumeInput.files.length === 0) return showFileErrorModal();
    if (resumeInput.files[0].type !== "application/pdf") return alert("PDF ÌååÏùºÎßå ÏßÄÏõêÎê©ÎãàÎã§.");

    showLoadingModal();
    try {
        const response = await fetch(lambdaFunctionUrl, {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ jobUrl })
        });
        const result = await response.json();
        window.jobPostingData = result;
        document.getElementById("jobTitle").innerHTML = result.title ? `<strong>${result.title}</strong>` : "<strong>Ï†úÎ™© ÏóÜÏùå</strong>";
        closeLoadingModal();
        showJobModal();
    } catch (error) {
        console.error("Í≥µÍ≥† Î∂ÑÏÑù Ïã§Ìå®:", error);
        closeLoadingModal();
        showErrorModal();
    }
});

